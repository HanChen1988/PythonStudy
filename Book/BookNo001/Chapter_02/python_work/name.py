"""
2-4 调整名字的大小写：将一个人名存储到一个变量中，再以小写、大写和首字母大写的方式显示这个人名。
"""
name = "Eric"
# 小写的方式显示这个人名
print(name.lower())
# 大写的方式显示这个人名
print(name.upper())
# 首字母大写的方式显示这个人名
print(name.title())
